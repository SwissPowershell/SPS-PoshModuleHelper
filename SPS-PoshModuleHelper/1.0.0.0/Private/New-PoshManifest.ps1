Class PMHPSData {
    [String[]] ${Tags}
    [URI] ${LicenseUri}
    [URI] ${ProjectUri}
    [URI] ${IconUri}
    [String] ${ReleaseNotes}
    PMHPSData() {}
    PMHPSData([String[]] ${Tags},[URI] ${LicenseUri},[URI] ${ProjectUri},[URI] ${IconUri}, [String] ${ReleaseNotes}) {
        $This.Tags = $Tags
        $This.LicenseUri = $LicenseUri
        $This.ProjectUri = $ProjectUri
        $This.IconUri = $IconUri
        $This.ReleaseNotes = $ReleaseNotes
    }
    [String] ParamToString ([String] ${Name}) {
        $Value = $This.$Name
        If ($Null -like $Value) {
            $RetVal = ''
        }Else{
            If ($Value -is [String[]]) {
                if ($Value.Count -gt 0){
                    $RetVal = "$($Name) = $(($Value | ForEach-Object {"'$($_)'"}) -join ',')"
                }Else{
                    $RetVal = ''
                }
            }Elseif ($Value -is [URI]) {
                $RetVal = "$($Name) = $(($Value | Select-Object -ExpandProperty 'AbsoluteUri'))"
            }Else {
                $RetVal = "$($Name) = '$($Value)'"
            }
        }
        Return $RetVal
    }
    [String] ToString() {
